/*
 * 配列 a[] に7つの整数を代入し, 
 * 先頭アドレスから1Byteずつ取り出し文字型(%c)として出力するプログラム
 */

#include <stdio.h>

int main(void)
{
  int a[] = { 176849379, 179476199, 176980707, 177776614, 
              178749923, 177701347, 178225635 };

  for(int i=0; i<sizeof(a); i++){
      printf("%c",((char*)a)[i]);
  }

  return 0;
}
